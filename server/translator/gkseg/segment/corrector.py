def fix(model, ctx):
    if ctx != None:
        pattern = ''.join(ctx)
        if pattern in rules:
            results = rules[pattern]
            if len(results) == 3:
                model.fix(results[0], results[1], results[2])

def countErr(model, ctx):
    if ctx != None:
        pattern = ''.join(ctx)
        if pattern in rules:
            if not(pattern in model.err):
                model.err[pattern] = []
            model.err[pattern].append(model.excerpt())

